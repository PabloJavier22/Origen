package segundo_trimestre.numeros_aleatorios;

public class array_ejercicio1 {
    public static void main(String[] args){
        int i = 0;
        int[] num = new int[12];
        num[0] = 39;
        num[1] = -2;
        num[4] = 0;
        num[6] = 14;
        num[8] = 5;
        num[9] = 120;
        
        while(i < 12){
            System.out.println(num[i]);
            i++;
        }
    }
}
